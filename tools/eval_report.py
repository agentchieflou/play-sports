#!/usr/bin/env python
"""Scorecard aggregator for play-sports (Epic 120).

Reads eval/scorecards/*.json, regenerates eval/SCORECARD.md (per
agent/model/archetype pairing: runs, mean, latest), and raises regression
alerts when a pairing's latest composite drops more than THRESHOLD points
below the mean of its earlier runs (needs >= 2 earlier runs).

Usage:  python tools/eval_report.py [--strict]
  --strict  exit 1 when any regression alert fires (for CI/cron use).
"""

import json
import sys
from collections import defaultdict
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent
SCORECARD_DIR = REPO / "eval" / "scorecards"
OUT = REPO / "eval" / "SCORECARD.md"
THRESHOLD = 15


def main():
    strict = "--strict" in sys.argv
    cards = []
    for path in sorted(SCORECARD_DIR.glob("*.json")):
        cards.append(json.loads(path.read_text(encoding="utf-8")))
    if not cards:
        print("eval_report: no scorecards yet")
        return 0

    cards.sort(key=lambda c: c.get("merged_at") or c["scored_at"])
    pairings = defaultdict(list)
    for c in cards:
        pairings[(c["agent"], c["model"], c["archetype"])].append(c)

    lines = [
        "# Agent Evaluation Scorecard",
        "",
        "Generated by `tools/eval_report.py` from `eval/scorecards/` — do not edit by hand.",
        "Model-assignment guidance in `GEMINI.md` defers to this table.",
        "",
        "| Agent | Model | Archetype | Runs | Mean | Latest | Trend |",
        "|---|---|---|---|---|---|---|",
    ]
    alerts = []
    for (agent, model, archetype), runs in sorted(pairings.items()):
        composites = [r["composite"] for r in runs]
        mean = sum(composites) / len(composites)
        latest = composites[-1]
        trend = "→"
        if len(composites) >= 2:
            trend = "↑" if latest > composites[-2] else ("↓" if latest < composites[-2] else "→")
        lines.append(
            f"| {agent} | {model} | {archetype} | {len(runs)} | {mean:.0f} | {latest} | {trend} |")
        if len(composites) >= 3:
            prior_mean = sum(composites[:-1]) / (len(composites) - 1)
            if latest < prior_mean - THRESHOLD:
                alerts.append(
                    f"REGRESSION: {agent}/{model}/{archetype} latest {latest} vs prior mean {prior_mean:.0f} (PR #{runs[-1]['pr']})")

    lines.append("")
    lines.append("## Recent runs")
    lines.append("")
    lines.append("| PR | Agent | Model | Archetype | Composite | Title |")
    lines.append("|---|---|---|---|---|---|")
    for c in cards[-10:][::-1]:
        lines.append(
            f"| #{c['pr']} | {c['agent']} | {c['model']} | {c['archetype']} | {c['composite']} | {c['title'][:60]} |")

    if alerts:
        lines.append("")
        lines.append("## Alerts")
        lines.append("")
        for a in alerts:
            lines.append(f"- ⚠️ {a}")

    OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"eval_report: {len(cards)} scorecard(s), {len(pairings)} pairing(s) -> {OUT.relative_to(REPO)}")
    for a in alerts:
        print("  " + a)
    return 1 if (strict and alerts) else 0


if __name__ == "__main__":
    sys.exit(main())
